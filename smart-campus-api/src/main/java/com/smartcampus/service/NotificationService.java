    public void sendTicketStatusUpdatedNotifications(User actor, String ticketOwnerId, String technicianId, String ticketId, String oldStatus, String newStatus) {
        // Regardless of who did it, we notify standard parties.
        Role role = actor.getRole();

        // 1. Notify User (if actor != user)
        if (!actor.getId().equals(ticketOwnerId)) {
            create(Notification.builder()
                .recipientUserId(ticketOwnerId)
                .type(NotificationType.TICKET_STATUS_UPDATED)
                .title("Ticket Status Updated")
                .message("Your ticket status changed to " + newStatus + ".")
                .relatedEntityId(ticketId)
                .relatedEntityType("TICKET")
                .build());
        }

        // 2. Notify Admin/Manager if Technician updated it
        if (role == Role.TECHNICIAN) {
            create(Notification.builder()
                .targetRole("ADMIN")
                .type(NotificationType.TICKET_STATUS_UPDATED)
                .title("Ticket Updated")
                .message("A technician updated ticket to " + newStatus)
                .relatedEntityId(ticketId)
                .relatedEntityType("TICKET")
                .build());

            create(Notification.builder()
                .targetRole("MANAGER")
                .type(NotificationType.TICKET_STATUS_UPDATED)
                .title("Ticket Updated")
                .message("A technician updated ticket to " + newStatus)
                .relatedEntityId(ticketId)
                .relatedEntityType("TICKET")
                .build());
        }

        // 3. Notify Technician if Admin/Manager updated it
        if ((role == Role.ADMIN || role == Role.MANAGER) && technicianId != null && !actor.getId().equals(technicianId)) {
            create(Notification.builder()
                .recipientUserId(technicianId)
                .type(NotificationType.TICKET_STATUS_UPDATED)
                .title("Assigned Ticket Updated")
                .message("An administrator changed the status of your assigned ticket to " + newStatus)
                .relatedEntityId(ticketId)
                .relatedEntityType("TICKET")
                .build());
        }

        // 4. Notify Actor (Self Confirmation)
        create(Notification.builder()
            .recipientUserId(actor.getId())
            .type(NotificationType.TICKET_STATUS_UPDATED)
            .title("Update Confirmed")
            .message("You updated the ticket to " + newStatus)
            .relatedEntityId(ticketId)
            .relatedEntityType("TICKET")
            .build());
    }

