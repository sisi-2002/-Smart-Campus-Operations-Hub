    // Get all notifications for a user (personal + role-based)
    public List<NotificationResponse> getNotificationsForUser(String userId, String role) {
        List<Notification> personal = notificationRepository
            .findByRecipientUserIdOrderByCreatedAtDesc(userId);

        List<Notification> roleBased = notificationRepository
            .findByTargetRoleOrderByCreatedAtDesc(role);

        // Merge, remove duplicates, sort by date
        return java.util.stream.Stream.concat(personal.stream(), roleBased.stream())
            .distinct()
            .sorted((a, b) -> b.getCreatedAt().compareTo(a.getCreatedAt()))
            .map(this::toDto)
            .collect(Collectors.toList());
    }

    // Count unread for badge
    public long getUnreadCount(String userId, String role) {
        long personal  = notificationRepository.countByRecipientUserIdAndReadFalse(userId);
        long roleBased = notificationRepository.countByTargetRoleAndReadFalse(role);
        return personal + roleBased;
    }

    // Mark single notification as read
    public NotificationResponse markAsRead(String notificationId, String userId) {
        Notification notification = notificationRepository.findById(notificationId)
            .orElseThrow(() -> new RuntimeException("Notification not found"));

        // Security check — only owner or role match can mark as read
        if (!notification.getRecipientUserId().equals(userId)
                && notification.getTargetRole() == null) {
            throw new RuntimeException("Access denied");
        }

        notification.setRead(true);
        notificationRepository.save(notification);
        return toDto(notification);
    }

    // Mark ALL notifications as read for a user
    public void markAllAsRead(String userId, String role) {
        // Personal notifications
        List<Notification> personal = notificationRepository
            .findByRecipientUserIdAndReadFalseOrderByCreatedAtDesc(userId);
        personal.forEach(n -> n.setRead(true));
        notificationRepository.saveAll(personal);

        // Role-based notifications
        List<Notification> roleBased = notificationRepository
            .findByTargetRoleAndReadFalseOrderByCreatedAtDesc(role);
        roleBased.forEach(n -> n.setRead(true));
        notificationRepository.saveAll(roleBased);
    }

    // Delete a single notification
    public void deleteNotification(String notificationId, String userId) {
        Notification notification = notificationRepository.findById(notificationId)
            .orElseThrow(() -> new RuntimeException("Notification not found"));

        if (!notification.getRecipientUserId().equals(userId)) {
            throw new RuntimeException("Access denied");
        }
        notificationRepository.delete(notification);
    }

    // Delete all notifications for a user
    public void deleteAll(String userId) {
        notificationRepository.deleteAllByRecipientUserId(userId);
    }

    // Create a notification generated by the client (frontend)
    public NotificationResponse createClientNotification(String userId, com.smartcampus.dto.request.ClientNotificationRequest request) {
        // Fallback to SYSTEM_ANNOUNCEMENT or something safe if the type isn't standard
        NotificationType type;
        try {
            type = NotificationType.valueOf(request.getType().toUpperCase());
        } catch (IllegalArgumentException e) {
            type = NotificationType.SYSTEM_ANNOUNCEMENT; // default fallback
        }

        Notification notification = Notification.builder()
            .recipientUserId(userId)
            .type(type)
            .title(request.getTitle())
            .message(request.getMessage())
            .relatedEntityType("CLIENT")
            .build();

        notificationRepository.save(notification);
        return toDto(notification);
    }

    public ManagerBroadcastNoticeResponse sendManagerBroadcastNotice(User actor, ManagerBroadcastNoticeRequest request) {
        if (actor.getRole() != Role.MANAGER && actor.getRole() != Role.ADMIN) {
            throw new RuntimeException("Only managers or admins can send broadcast notices");
        }

        String title = String.valueOf(request.getTitle() == null ? "" : request.getTitle()).trim();
        String message = String.valueOf(request.getMessage() == null ? "" : request.getMessage()).trim();
        if (title.isBlank() || message.isBlank()) {
            throw new IllegalArgumentException("Title and message are required");
        }

        LocalDateTime windowStart = request.getWindowStart() != null ? request.getWindowStart() : LocalDateTime.now();
        LocalDateTime windowEnd = request.getWindowEnd() != null ? request.getWindowEnd() : windowStart.plusDays(14);
        if (windowEnd.isBefore(windowStart)) {
            throw new IllegalArgumentException("Window end must be after window start");
        }

        String query = String.valueOf(request.getResourceQuery() == null ? "" : request.getResourceQuery()).trim().toLowerCase(Locale.ROOT);

        List<Booking> bookingsInWindow = findUpcomingBookingsInWindow(windowStart, windowEnd);
        List<Booking> matchedBookings = query.isBlank()
            ? bookingsInWindow
            : filterBookingsByResourceQuery(bookingsInWindow, query);

        Set<String> userIds = matchedBookings.stream()
            .map(Booking::getUser)
            .filter(Objects::nonNull)
            .map(User::getId)
            .filter(id -> id != null && !id.isBlank())
            .collect(Collectors.toCollection(HashSet::new));

        List<Notification> notifications = new ArrayList<>();
        for (String userId : userIds) {
            notifications.add(Notification.builder()
                .recipientUserId(userId)
                .type(NotificationType.SYSTEM_ANNOUNCEMENT)
                .title(title)
                .message(message)
                .relatedEntityType("BROADCAST_NOTICE")
                .relatedEntityId(actor.getId())
                .build());
        }

        if (!notifications.isEmpty()) {
            notificationRepository.saveAll(notifications);
        }

        create(Notification.builder()
            .recipientUserId(actor.getId())
            .type(NotificationType.SYSTEM_ANNOUNCEMENT)
            .title("Broadcast Sent")
            .message("Your notice was sent to " + userIds.size() + " users.")
            .relatedEntityType("BROADCAST_NOTICE")
            .build());

        return ManagerBroadcastNoticeResponse.builder()
            .title(title)
            .resourceQuery(request.getResourceQuery())
            .windowStart(windowStart)
            .windowEnd(windowEnd)
            .matchedBookings(matchedBookings.size())
            .notifiedUsers(userIds.size())
            .build();
    }

